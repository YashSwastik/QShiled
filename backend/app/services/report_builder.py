# ── Severity helpers ──────────────────────────────────────────────────────────

def _sev_color(sev: str) -> HexColor:
    s = (sev or "").lower()
    return {
        "critical": C_CRIT,
        "high":     C_HIGH,
        "moderate": C_MOD,
        "medium":   C_MOD,
        "low":      C_LOW,
        "safe":     C_SAFE,
    }.get(s, C_MUTED)


def _now_utc() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")


# ── Shared page callbacks ─────────────────────────────────────────────────────

def _make_page_callbacks(report_title: str, project_name: str):
    """Returns (on_first_page, on_later_pages) for ReportLab page templates."""

    def _footer(canvas, doc):
        canvas.saveState()
        canvas.setFont("Helvetica", 7)
        canvas.setFillColor(C_MUTED)
        left = doc.leftMargin
        right = PAGE_W - doc.rightMargin
        y = 12 * mm

        canvas.drawString(left, y, f"Generated by QShield  |  {report_title}")
        canvas.drawRightString(right, y, f"Page {doc.page}")
        # thin rule
        canvas.setStrokeColor(C_BORDER)
        canvas.setLineWidth(0.4)
        canvas.line(left, y + 4 * mm, right, y + 4 * mm)
        canvas.restoreState()

    def on_first_page(canvas, doc):
        _footer(canvas, doc)

    def on_later_pages(canvas, doc):
        _footer(canvas, doc)

    return on_first_page, on_later_pages


