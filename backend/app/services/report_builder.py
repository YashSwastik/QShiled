def build_inventory_pdf(data: dict) -> bytes:
    """
    Build Technical Cryptographic Inventory Report PDF.

    data keys:
      scan, project, dashboard, findings (list of finding dicts),
      risk_summary (dict with per_finding severity)
    """
    buf = io.BytesIO()
    s = _styles()

    dash     = data.get("dashboard", {})
    findings = data.get("findings", [])
    sev_map  = data.get("severity_map", {})   # finding_id -> severity

    project_name = data.get("project", {}).get("name") or dash.get("application_name", "—")
    app_name     = dash.get("application_name", "—")
    scan_name    = dash.get("scan_name", "—")
    gen_ts       = _now_utc()

    on_first, on_later = _make_page_callbacks("Technical Cryptographic Inventory Report", project_name)

    doc = BaseDocTemplate(
        buf, pagesize=A4,
        leftMargin=2*cm, rightMargin=2*cm, topMargin=2*cm, bottomMargin=2.5*cm,
        title="Technical Cryptographic Inventory Report",
        author="QShield",
    )
    frame = Frame(doc.leftMargin, doc.bottomMargin, doc.width, doc.height, id="normal")
    doc.addPageTemplates([PageTemplate(id="main", frames=frame,
                                       onPage=on_first, onPageEnd=on_later)])
    story = []

    # Cover
    story += _cover_flows(
        "Technical Cryptographic\nInventory Report",
        "Cryptographic Bill of Materials (CBOM) — Audit Reference",
        scan_name, project_name, app_name, gen_ts,
    )
    story.append(PageBreak())

    # ── 1. Inventory Statistics ───────────────────────────────────────────────
    story.append(Paragraph("1. Inventory Statistics", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    total = len(findings)
    quantum_vuln = sum(1 for f in findings if f.get("quantum_status") == "vulnerable")
    borderline   = sum(1 for f in findings if f.get("quantum_status") == "borderline")
    safe_count   = sum(1 for f in findings if f.get("quantum_status") in ("safe", "hybrid"))

    stat_data = [
        ["Metric", "Value"],
        ["Total Cryptographic Findings", str(total)],
        ["Quantum-Vulnerable",           str(quantum_vuln)],
        ["Borderline / Transitional",    str(borderline)],
        ["Quantum-Safe",                 str(safe_count)],
    ]
    story.append(_table(stat_data, col_widths=[11*cm, 5*cm]))
    story.append(Spacer(1, 0.4 * cm))

    # ── 2. Algorithm Inventory by Category ───────────────────────────────────
    story.append(Paragraph("2. Algorithm Inventory by Category", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    # Group by category — maps FindingCategory enum string values to display buckets.
    # FindingCategory values (from models/finding.py):
    #   QUANTUM_VULNERABLE_PUBLIC_KEY, SYMMETRIC, HASH, LEGACY_DEPRECATED,
    #   POST_QUANTUM, UNKNOWN_REVIEW
    CRYPTO_CATEGORIES = {
        "public_key":   "Public-Key Cryptography",
        "symmetric":    "Symmetric Cryptography",
        "hash":         "Hash Functions",
        "post_quantum": "Post-Quantum Cryptography",
        "legacy":       "Legacy / Deprecated",
        "other":        "Other / Unknown",
    }
    # Enum value → internal bucket key (covers both uppercase enum strings
    # and any lowercase aliases that may appear in serialised data)
    _CAT_MAP: dict[str, str] = {
        "QUANTUM_VULNERABLE_PUBLIC_KEY": "public_key",
        "SYMMETRIC":                     "symmetric",
        "HASH":                          "hash",
        "POST_QUANTUM":                  "post_quantum",
        "LEGACY_DEPRECATED":             "legacy",
        "UNKNOWN_REVIEW":                "other",
        # lowercase / legacy aliases
        "public_key":   "public_key",
        "symmetric":    "symmetric",
        "hash":         "hash",
        "certificate":  "public_key",
        "configuration": "other",
        "other":        "other",
    }
    by_cat: dict[str, list] = {k: [] for k in CRYPTO_CATEGORIES}
    for f in findings:
        raw_cat = (f.get("category") or "other")
        bucket = _CAT_MAP.get(raw_cat, _CAT_MAP.get(raw_cat.upper(), "other"))
        by_cat[bucket].append(f)


    for cat_key, cat_label in CRYPTO_CATEGORIES.items():
        cat_findings = by_cat.get(cat_key, [])
        if not cat_findings:
            continue
        story.append(Paragraph(cat_label, s["h3"]))

        # group by algorithm within category
        algo_counts: dict[str, dict] = {}
        for f in cat_findings:
            alg = f.get("algorithm", "Unknown")
            if alg not in algo_counts:
                algo_counts[alg] = {
                    "family": f.get("algorithm_family", "—"),
                    "count": 0,
                    "locations": [],
                    "purpose": f.get("usage_context") or "—",
                    "qs": f.get("quantum_status", "unknown"),
                }
            algo_counts[alg]["count"] += 1
            fp = f.get("file_path") or "—"
            if fp not in algo_counts[alg]["locations"] and len(algo_counts[alg]["locations"]) < 3:
                algo_counts[alg]["locations"].append(fp[-40:])

        algo_rows = [["Algorithm", "Family", "Count", "Quantum Status", "Example Location"]]
        algo_extra = []
        for ai, (alg, info) in enumerate(sorted(algo_counts.items()), start=1):
            qs = info["qs"]
            qs_color = {"vulnerable": C_CRIT, "borderline": C_MOD, "safe": C_SAFE, "hybrid": C_SAFE}.get(qs, C_MUTED)
            algo_rows.append([
                alg,
                info["family"],
                str(info["count"]),
                qs.capitalize(),
                info["locations"][0] if info["locations"] else "—",
            ])
            algo_extra.append(("TEXTCOLOR", (3, ai), (3, ai), qs_color))
            algo_extra.append(("FONTNAME",  (3, ai), (3, ai), "Helvetica-Bold"))

        story.append(_table(
            algo_rows,
            col_widths=[3.5*cm, 2.5*cm, 1.5*cm, 2.5*cm, 6*cm],
            extra_style=algo_extra if algo_extra else None,
        ))
        story.append(Spacer(1, 0.3 * cm))

    # ── 3. Quantum Migration Risks (separate from Legacy Security) ────────────
    story.append(PageBreak())
    story.append(Paragraph("3. Quantum Migration Risks", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
    story.append(Paragraph(
        "The following findings represent cryptographic usage that is considered "
        "quantum-vulnerable or borderline. These require active migration planning.",
        s["body"]
    ))
    story.append(Spacer(1, 0.2 * cm))

    qm_findings = [f for f in findings if f.get("quantum_status") in ("vulnerable", "borderline")]
    if qm_findings:
        qm_rows = [["Algorithm", "Status", "Severity", "File", "Line"]]
        qm_extra = []
        for qi, f in enumerate(qm_findings[:40], start=1):  # cap at 40 rows
            sev = sev_map.get(f.get("id", ""), "—")
            fp  = (f.get("file_path") or "—")[-45:]
            ln  = str(f.get("line_number") or "—")
            qs  = f.get("quantum_status", "—")
            qm_rows.append([
                f.get("algorithm", "—"),
                qs.capitalize(),
                sev,
                fp, ln,
            ])
            qm_extra.append(("TEXTCOLOR", (2, qi), (2, qi), _sev_color(sev)))
        story.append(_table(
            qm_rows, col_widths=[3*cm, 2.5*cm, 2.5*cm, 7*cm, 1*cm],
            extra_style=qm_extra if qm_extra else None,
        ))
    else:
        story.append(Paragraph("No quantum-vulnerable or borderline findings identified.", s["body"]))
    story.append(Spacer(1, 0.4 * cm))

    # ── 4. Legacy Security Concerns ───────────────────────────────────────────
    LEGACY_ALGOS = {"md5", "sha1", "sha-1", "rc4", "des", "3des", "triple-des", "rc2"}
    legacy_findings = [
        f for f in findings
        if (f.get("algorithm") or "").lower() in LEGACY_ALGOS
        or (f.get("algorithm_family") or "").lower() in {"md5", "sha1"}
    ]

    story.append(Paragraph("4. Legacy Security Concerns", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
    story.append(Paragraph(
        "The following algorithms are deprecated for classical security reasons (weak key sizes, "
        "known collision vulnerabilities, etc.) independent of quantum threats.",
        s["body"]
    ))
    story.append(Spacer(1, 0.2 * cm))
    if legacy_findings:
        leg_rows = [["Algorithm", "File", "Line", "Usage Context"]]
        for f in legacy_findings[:40]:
            leg_rows.append([
                f.get("algorithm", "—"),
                (f.get("file_path") or "—")[-45:],
                str(f.get("line_number") or "—"),
                (f.get("usage_context") or "—")[:60],
            ])
        story.append(_table(leg_rows, col_widths=[3*cm, 7*cm, 1.5*cm, 4.5*cm]))
    else:
        story.append(Paragraph("No legacy-only algorithm findings identified.", s["body"]))
    story.append(Spacer(1, 0.4 * cm))

    # ── 5. Limitations ────────────────────────────────────────────────────────
    story.append(Paragraph("5. Limitations", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
    for note in [
        "Finding details are based on static analysis of uploaded source code, configuration files, and certificates.",
        "Certificate private key material is never extracted, stored, or included in this report.",
        "Findings are capped at 40 rows per section for readability. Full data available via the Inventory page.",
        "File paths are truncated to 45 characters for display purposes.",
        "Confidence scores reflect the scanner's pattern-match confidence, not an attestation of actual usage.",
    ]:
        story.append(Paragraph(f"• {note}", s["disclaimer"]))
        story.append(Spacer(1, 0.1 * cm))

    doc.build(story)
    return buf.getvalue()


