"""
report_builder.py — QShield PDF Report Generation Service
==========================================================

Generates professional PDF reports directly from structured data using ReportLab.

Three report generators (called by router endpoints and ZIP bundle):
  - build_executive_pdf(data: dict) -> bytes
  - build_inventory_pdf(data: dict) -> bytes
  - build_roadmap_pdf(data: dict)   -> bytes

Each returns PDF bytes.  The caller writes to a streaming response or ZIP buffer.

Design principles:
  - Pure function: takes structured data dict, returns PDF bytes.
  - No DB access — data is pre-loaded by the router, which reuses existing engines.
  - No LLM. No score recalculation. No fake data.
  - Secrets / private keys are never passed into these functions (router is responsible).
"""
