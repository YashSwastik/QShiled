# REPORT 1: Executive Quantum Readiness
# ═══════════════════════════════════════════════════════════════════════════════

def build_executive_pdf(data: dict) -> bytes:
    """
    Build Executive Quantum Readiness Report PDF.

    data keys expected (from /api/reports/executive JSON):
      scan, dashboard (DashboardSummary dict), recommendations (list)
    """
    buf = io.BytesIO()
    s = _styles()
    usable_w = PAGE_W - 4 * cm

    dash   = data.get("dashboard", {})
    recs   = data.get("recommendations", [])
    scan   = data.get("scan", {})
    proj   = data.get("project", {})

    project_name = proj.get("name") or dash.get("application_name", "—")
    app_name     = dash.get("application_name", "—")
    scan_name    = dash.get("scan_name", "—")
    gen_ts       = _now_utc()
    qrs          = dash.get("quantum_readiness_score", 0)
    rl           = dash.get("readiness_label", "—")

    on_first, on_later = _make_page_callbacks("Executive Quantum Readiness Report", project_name)

    doc = BaseDocTemplate(
        buf, pagesize=A4,
        leftMargin=2*cm, rightMargin=2*cm, topMargin=2*cm, bottomMargin=2.5*cm,
        title="Executive Quantum Readiness Report",
        author="QShield",
    )
    frame = Frame(doc.leftMargin, doc.bottomMargin,
                  doc.width, doc.height, id="normal")
    doc.addPageTemplates([
        PageTemplate(id="main", frames=frame,
                     onPage=on_first, onPageEnd=on_later),
    ])

    story = []

    # ── Cover ─────────────────────────────────────────────────────────────────
    story += _cover_flows(
        "Executive Quantum\nReadiness Report",
        "Quantum Cryptography Posture Assessment",
        scan_name, project_name, app_name, gen_ts,
    )
    story.append(PageBreak())

    # ── 1. Executive Summary ──────────────────────────────────────────────────
    story.append(Paragraph("1. Executive Summary", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    total = dash.get("total_findings", 0)
    qrelf = dash.get("quantum_relevant_findings", 0)
    crit  = dash.get("critical_findings", 0)
    high  = dash.get("high_findings", 0)
    prog  = dash.get("migration_progress_pct", 0.0)

    story.append(Paragraph(
        f"This report summarises the quantum cryptography posture of <b>{app_name}</b> "
        f"based on scan <i>{scan_name}</i>. A total of <b>{total}</b> cryptographic findings "
        f"were identified, of which <b>{qrelf}</b> are quantum-migration relevant. "
        f"The organisation's current Quantum Readiness Score is <b>{qrs}/100</b> "
        f"({rl}). Migration is <b>{prog:.1f}%</b> complete as measured by weighted roadmap stage progress.",
        s["body"]
    ))
    story.append(Spacer(1, 0.3 * cm))

    # ── 2. Quantum Readiness Score ────────────────────────────────────────────
    story.append(Paragraph("2. Quantum Readiness Score", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    meth = dash.get("readiness_methodology", {})
    score_data = [
        ["Component", "Weight", "Value", "Contribution"],
        ["Exposure (non-vulnerable / total)", "60%",
         f"{meth.get('s_exposure', 0):.4f}",
         f"{meth.get('s_exposure', 0)*60:.1f}"],
        ["Risk Inversion (1 − avg_risk/100)", "25%",
         f"{meth.get('s_risk_inv', 0):.4f}",
         f"{meth.get('s_risk_inv', 0)*25:.1f}"],
        ["Migration Progress (migrated/total)", "15%",
         f"{meth.get('s_progress', 0):.4f}",
         f"{meth.get('s_progress', 0)*15:.1f}"],
        ["TOTAL SCORE", "100%", "—", str(qrs)],
    ]
    score_col_w = [9*cm, 2*cm, 2.5*cm, 2.5*cm]
    score_t = Table(score_data, colWidths=score_col_w, repeatRows=1)
    score_t.setStyle(TableStyle(
        _BASE_TABLE.getCommands() + [
            ("BACKGROUND", (0, 4), (-1, 4), C_ACCENT),
            ("TEXTCOLOR",  (0, 4), (-1, 4), C_SURF),
            ("FONTNAME",   (0, 4), (-1, 4), "Helvetica-Bold"),
        ]
    ))
    story.append(score_t)
    story.append(Spacer(1, 0.3 * cm))
    story.append(Paragraph(
        meth.get("disclaimer",
            "The Quantum Readiness Score is an internal deterministic prioritization metric "
            "derived from cryptographic exposure, migration risk, and migration progress. "
            "It is intended for planning purposes and is not an official NIST score."),
        s["disclaimer"],
    ))
    story.append(Spacer(1, 0.4 * cm))

    # ── 3. Overall Risk Summary ───────────────────────────────────────────────
    story.append(Paragraph("3. Overall Risk Summary", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    risk_data = [
        ["Metric", "Value"],
        ["Total Findings",             str(total)],
        ["Quantum-Relevant Findings",  str(qrelf)],
        ["Quantum-Safe Findings",      str(dash.get("quantum_safe_findings", 0))],
        ["Critical Severity",          str(crit)],
        ["High Severity",              str(high)],
        ["Moderate Severity",          str(dash.get("moderate_findings", 0))],
        ["Low Severity",               str(dash.get("low_findings", 0))],
        ["Migration Progress",         f"{prog:.1f}%"],
        ["Migrated Items",             str(dash.get("migrated_items", 0))],
        ["Total Roadmap Items",        str(dash.get("total_roadmap_items", 0))],
    ]
    risk_t = Table(risk_data, colWidths=[11*cm, 5*cm], repeatRows=1)
    extra = []
    for i, (label, val) in enumerate(risk_data[1:], start=1):
        if "Critical" in label and int(val or 0) > 0:
            extra.append(("BACKGROUND", (1, i), (1, i), C_CRIT))
            extra.append(("TEXTCOLOR",  (1, i), (1, i), C_SURF))
        elif "High" in label and int(val or 0) > 0:
            extra.append(("BACKGROUND", (1, i), (1, i), C_HIGH))
            extra.append(("TEXTCOLOR",  (1, i), (1, i), C_SURF))
    risk_t.setStyle(TableStyle(_BASE_TABLE.getCommands() + extra))
    story.append(risk_t)
    story.append(Spacer(1, 0.4 * cm))

    # ── 4. Algorithm Distribution ─────────────────────────────────────────────
    algo_dist = dash.get("algorithm_distribution", [])
    if algo_dist:
        story.append(Paragraph("4. Algorithm Distribution", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
        algo_rows = [["Algorithm Family", "Findings"]]
        for a in algo_dist:
            algo_rows.append([a.get("family", "—"), str(a.get("count", 0))])
        story.append(_table(algo_rows, col_widths=[13*cm, 3*cm]))
        story.append(Spacer(1, 0.4 * cm))

    # ── 5. Top Risk Findings ──────────────────────────────────────────────────
    top_findings = dash.get("top_findings", [])
    if top_findings:
        story.append(Paragraph("5. Top Risk Findings", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
        tf_rows = [["Algorithm", "Family", "File", "Risk Score", "Severity"]]
        tf_extra = []
        for i, f in enumerate(top_findings, start=1):
            sev = f.get("severity", "Low")
            fpath = (f.get("file_path") or "—")[-50:]  # truncate long paths
            tf_rows.append([
                f.get("algorithm", "—"),
                f.get("algorithm_family", "—"),
                fpath,
                str(f.get("risk_score", 0)),
                sev,
            ])
            tf_extra.append(("TEXTCOLOR", (4, i), (4, i), _sev_color(sev)))
            tf_extra.append(("FONTNAME",  (4, i), (4, i), "Helvetica-Bold"))

        story.append(_table(
            tf_rows,
            col_widths=[3*cm, 3*cm, 6*cm, 2.5*cm, 2.5*cm],
            extra_style=tf_extra if tf_extra else None,
        ))
        story.append(Spacer(1, 0.4 * cm))

    # ── 6. Migration Waves ────────────────────────────────────────────────────
    wave_dist = dash.get("wave_distribution", [])
    stage_dist = dash.get("stage_distribution", [])
    if wave_dist or stage_dist:
        story.append(Paragraph("6. Migration Progress", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
        if wave_dist:
            wave_rows = [["Wave", "Label", "Items"]]
            for w in wave_dist:
                wave_rows.append([str(w.get("wave", "—")), w.get("label", "—"), str(w.get("count", 0))])
            story.append(Paragraph("Migration Waves", s["h3"]))
            story.append(_table(wave_rows, col_widths=[2*cm, 8*cm, 6*cm]))
            story.append(Spacer(1, 0.3 * cm))
        if stage_dist:
            stg_rows = [["Stage", "Items"]]
            for st in stage_dist:
                stg_rows.append([st.get("stage", "—"), str(st.get("count", 0))])
            story.append(Paragraph("Lifecycle Stages", s["h3"]))
            story.append(_table(stg_rows, col_widths=[8*cm, 8*cm]))
            story.append(Spacer(1, 0.4 * cm))

    # ── 7. Executive Recommendations ─────────────────────────────────────────
    quantum_recs = [r for r in recs if r.get("is_quantum_concern")][:5]
    if quantum_recs:
        story.append(PageBreak())
        story.append(Paragraph("7. Executive Recommendations", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
        for i, r in enumerate(quantum_recs, 1):
            algo  = r.get("algorithm", "—")
            tgt   = r.get("recommended_target_category", "Manual Review")
            prio  = r.get("migration_priority", "low").upper()
            stds  = ", ".join(r.get("nist_standards", [])) or "—"
            steps = r.get("migration_steps", [])[:3]
            story.append(KeepTogether([
                Paragraph(f"{i}. {algo} → {tgt}  [Priority: {prio}]", s["h3"]),
                Paragraph(r.get("current_state_description", ""), s["body"]),
                Paragraph(f"NIST Standards: {stds}", s["small"]),
                *(Paragraph(f"• {step}", s["body"]) for step in steps),
                Spacer(1, 0.2 * cm),
            ]))

    # ── 8. Limitations ────────────────────────────────────────────────────────
    story.append(Paragraph("8. Limitations", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
    for note in [
        "QShield Quantum Readiness Scores are internal deterministic prioritization metrics — not official NIST scores or compliance certifications.",
        "Cryptographic findings are identified by static analysis. Dynamic runtime usage may differ.",
        "Scan results reflect the state of assets at the time of scan upload.",
        "Production migration requires protocol, interoperability, key-management, deployment, and security validation beyond what QShield automates.",
        "Certificate private key material is never extracted, stored, or included in reports.",
    ]:
        story.append(Paragraph(f"• {note}", s["disclaimer"]))
        story.append(Spacer(1, 0.1 * cm))

    doc.build(story)
    return buf.getvalue()


