# REPORT 3: Migration Roadmap
# ═══════════════════════════════════════════════════════════════════════════════

def build_roadmap_pdf(data: dict) -> bytes:
    """
    Build Migration Roadmap Report PDF.

    data keys:
      scan, project, dashboard, roadmap (ScanRoadmapResponse dict),
      recommendations (list), top_recs (list)
    """
    buf = io.BytesIO()
    s = _styles()

    dash    = data.get("dashboard", {})
    roadmap = data.get("roadmap", {})
    recs    = data.get("recommendations", [])

    project_name = data.get("project", {}).get("name") or dash.get("application_name", "—")
    app_name     = dash.get("application_name", "—")
    scan_name    = dash.get("scan_name", "—")
    gen_ts       = _now_utc()

    items       = roadmap.get("items", [])
    wave_summ   = roadmap.get("wave_summaries", [])

    on_first, on_later = _make_page_callbacks("Migration Roadmap Report", project_name)

    doc = BaseDocTemplate(
        buf, pagesize=A4,
        leftMargin=2*cm, rightMargin=2*cm, topMargin=2*cm, bottomMargin=2.5*cm,
        title="Migration Roadmap Report",
        author="QShield",
    )
    frame = Frame(doc.leftMargin, doc.bottomMargin, doc.width, doc.height, id="normal")
    doc.addPageTemplates([PageTemplate(id="main", frames=frame,
                                       onPage=on_first, onPageEnd=on_later)])
    story = []

    # Cover
    story += _cover_flows(
        "Migration Roadmap Report",
        "Post-Quantum Cryptography Migration Plan",
        scan_name, project_name, app_name, gen_ts,
    )
    story.append(PageBreak())

    # ── 1. Migration Overview ─────────────────────────────────────────────────
    story.append(Paragraph("1. Migration Overview", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    total_items = len(items)
    migrated    = sum(1 for it in items if it.get("stage") == "MIGRATED")
    prog        = dash.get("migration_progress_pct", 0.0)
    qrs         = dash.get("quantum_readiness_score", 0)

    ov_data = [
        ["Metric", "Value"],
        ["Total Roadmap Items", str(total_items)],
        ["Migrated",            str(migrated)],
        ["Migration Progress",  f"{prog:.1f}%"],
        ["Quantum Readiness Score", f"{qrs}/100 ({dash.get('readiness_label', '—')})"],
    ]
    story.append(_table(ov_data, col_widths=[11*cm, 5*cm]))
    story.append(Spacer(1, 0.4 * cm))

    # ── 2. Wave Summary ───────────────────────────────────────────────────────
    if wave_summ:
        story.append(Paragraph("2. Migration Waves", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

        WAVE_LABELS = {1: "NOW — Immediate priorities", 2: "NEXT — Near-term work", 3: "LATER — Planned work"}
        for ws in sorted(wave_summ, key=lambda x: x.get("wave", 99)):
            wn = ws.get("wave", "?")
            story.append(Paragraph(f"Wave {wn}: {WAVE_LABELS.get(wn, '')}", s["h3"]))
            story.append(Paragraph(ws.get("description", ""), s["body"]))
            w_data = [["Metric", "Value"],
                      ["Total Items", str(ws.get("item_count", ws.get("total_items", 0)))]]
            story.append(_table(w_data, col_widths=[8*cm, 8*cm]))
            story.append(Spacer(1, 0.2 * cm))

    # ── 3. Lifecycle Stage Distribution ───────────────────────────────────────
    stage_dist = dash.get("stage_distribution", [])
    if stage_dist:
        story.append(Paragraph("3. Lifecycle Stage Distribution", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
        stg_rows = [["Stage", "Items", "Description"]]
        STAGE_DESC = {
            "DISCOVERED": "Finding catalogued",
            "ASSESSED":   "Risk and impact evaluated",
            "PLANNED":    "Migration approach selected",
            "PILOT":      "In controlled pilot",
            "TRANSITION": "Active migration in progress",
            "VALIDATION": "Security validation in progress",
            "MIGRATED":   "Migration complete",
        }
        for st in stage_dist:
            nm = st.get("stage", "—")
            stg_rows.append([nm, str(st.get("count", 0)), STAGE_DESC.get(nm, "—")])
        story.append(_table(stg_rows, col_widths=[4*cm, 2*cm, 10*cm]))
        story.append(Spacer(1, 0.4 * cm))

    # ── 4. Roadmap Items by Wave ──────────────────────────────────────────────
    story.append(PageBreak())
    story.append(Paragraph("4. Roadmap Items Detail", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))

    for wave_num in [1, 2, 3]:
        wave_items = [it for it in items if it.get("wave") == wave_num]
        if not wave_items:
            continue
        story.append(Paragraph(f"Wave {wave_num} Items", s["h3"]))
        rd_rows = [["Algorithm", "Stage", "Effort", "Target", "File"]]
        for it in wave_items[:30]:  # cap per wave
            rd_rows.append([
                it.get("algorithm", "—"),
                it.get("stage", "DISCOVERED"),
                it.get("effort_estimate", "—"),
                it.get("recommended_target_category", "—"),
                (it.get("file_path") or "—")[-40:],
            ])
        story.append(_table(rd_rows, col_widths=[3*cm, 2.5*cm, 2*cm, 4*cm, 4.5*cm]))
        story.append(Spacer(1, 0.3 * cm))

    # ── 5. Priority Actions ───────────────────────────────────────────────────
    quantum_recs = [r for r in recs if r.get("is_quantum_concern")][:5]
    if quantum_recs:
        story.append(Paragraph("5. Priority Actions", s["h2"]))
        story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
        for i, r in enumerate(quantum_recs, 1):
            algo = r.get("algorithm", "—")
            tgt  = r.get("recommended_target_category", "—")
            prio = r.get("migration_priority", "low").upper()
            stds = ", ".join(r.get("nist_standards", [])) or "—"
            story.append(KeepTogether([
                Paragraph(f"{i}. {algo} → {tgt}  [Priority: {prio}  |  NIST: {stds}]", s["h3"]),
                Paragraph(r.get("quantum_threat", ""), s["body"]),
                Spacer(1, 0.2 * cm),
            ]))

    # ── 6. Limitations ────────────────────────────────────────────────────────
    story.append(Paragraph("6. Limitations", s["h2"]))
    story.append(HRFlowable(width="100%", thickness=0.5, color=C_ACCENT, spaceAfter=6))
    for note in [
        "Roadmap wave assignment is deterministic — derived from risk score and application context.",
        "User-controlled lifecycle stages (ASSESSED, PLANNED, etc.) are persisted and reflected here.",
        "Migration effort estimates are indicative — actual effort depends on codebase complexity and team capacity.",
        "Timeline guidance is advisory only and not a project plan commitment.",
        "QShield does not automatically perform migration. All changes require developer implementation and security review.",
    ]:
        story.append(Paragraph(f"• {note}", s["disclaimer"]))
        story.append(Spacer(1, 0.1 * cm))

    doc.build(story)
    return buf.getvalue()
